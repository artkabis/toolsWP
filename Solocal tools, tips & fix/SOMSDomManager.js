    /* ============================= EX√âCUTION ET OBSERVATION =================================== */
    /* ========================================================================================== */

    /**
     * Ex√©cute une op√©ration sur les √©l√©ments actuels
     */
    executeOperation(operation) {
        const { targetSelector, targetChild, action } = operation;
        
        document.querySelectorAll(targetSelector).forEach(parent => {
            const elements = targetChild 
                ? Array.from(parent.querySelectorAll(targetChild))
                : [parent];
            
            action(elements);
        });

        if (this.config.debug) {
            console.log('‚úÖ Op√©ration ex√©cut√©e:', operation.id);
        }
    }

    /**
     * Observe et ex√©cute automatiquement sur les mutations
     */
    observeOperation(operation) {
        const { targetSelector, debounce } = operation;
        let timeoutId = null;

        const callback = (mutationsList) => {
            const execute = () => {
                for (const mutation of mutationsList) {
                    if (mutation.type === 'childList') {
                        mutation.addedNodes.forEach(node => {
                            if (node.nodeType === Node.ELEMENT_NODE) {
                                const matches = node.matches(targetSelector)
                                    ? [node]
                                    : Array.from(node.querySelectorAll(targetSelector));
                                
                                if (matches.length > 0) {
                                    this.executeOperation(operation);
                                }
                            }
                        });
                    }
                }
            };

            if (debounce > 0) {
                clearTimeout(timeoutId);
                timeoutId = setTimeout(execute, debounce);
            } else {
                execute();
            }
        };

        const observer = new MutationObserver(callback);
        observer.observe(document.body, { childList: true, subtree: true });
        
        this.observers.push({ operation: operation.id, observer });

        if (this.config.debug) {
            console.log('üëÅÔ∏è Observer cr√©√© pour:', operation.id);
        }
    }

    /**
     * Lance toutes les op√©rations enregistr√©es
     */
    run() {
        if (this.config.debug) {
            console.log('üèÉ Ex√©cution de', this.operations.length, 'op√©ration(s)');
        }

        this.operations.forEach(operation => {
            // Ex√©cution initiale
            this.executeOperation(operation);

            // Observer si activ√©
            if (operation.autoObserve) {
                this.observeOperation(operation);
            }
        });

        return this;
    }

    /**
     * Arr√™te tous les observers
     */
    stop() {
        this.observers.forEach(({ observer }) => observer.disconnect());
        this.observers = [];
        
        if (this.config.debug) {
            console.log('‚èπÔ∏è Tous les observers arr√™t√©s');
        }
        
        return this;
    }

    /**
     * R√©initialise toutes les op√©rations
     */
    reset() {
        this.stop();
        this.operations = [];
        
        if (this.config.debug) {
            console.log('üîÑ Manager r√©initialis√©');
        }
        
        return this;
    }

    /* ========================================================================================== */
    /* ============================= M√âTHODES UTILITAIRES ======================================= */
